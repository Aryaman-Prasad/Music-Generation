def generate_midi(base_note: str, length: int, velocity: int, mdp: dict, out_file: str):
    assert(base_note in mdp.keys())

    pm = pretty_midi.PrettyMIDI()
    inst = pretty_midi.Instrument(program=1, is_drum=False)
    pm.instruments.append(inst)
    rng = np.random.default_rng()

    curr = 0
    dt = 0.5
    inst.notes.append(pretty_midi.Note(velocity, pretty_midi.note_name_to_number(base_note), curr, curr+dt))
    curr += dt
    curr_note = base_note

    for i in range(length-1):
        new_note = rng.choice(list(mdp.keys()), p=mdp[curr_note])
        inst.notes.append(pretty_midi.Note(velocity, pretty_midi.note_name_to_number(new_note), curr, curr+dt))
        curr += dt
        curr_note = new_note

    pm.write(out_file)

